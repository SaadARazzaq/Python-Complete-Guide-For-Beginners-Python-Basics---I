#Guess the Type

#Python provides a way to determine the type of a variable using the type() function:

# Guessing the type of variables
x = 5
print(type(x))  # Output: <class 'int'>

y = "Python"
print(type(y))  # Output: <class 'str'>

#Type inference helps in understanding the data stored in variables.

#####################################################################################################

#Operations with Other Types

#Python supports operations and methods for various data types. Let's explore some examples:

# String concatenation
greeting = "Hello"
name = "Python"
message = greeting + ", " + name
print(message)  # Output: Hello, Python

# List manipulation
numbers = [1, 2, 3]
numbers.append(4)
print(numbers)  # Output: [1, 2, 3, 4]

#Python's rich set of operations makes it easy to work with different data types effectively.

#Type Conversion

#Python allows converting data from one type to another using type conversion functions:

# Type conversion
x = 10
y = str(x)  # Convert integer to string
print(y)  # Output: "10"

z = float("3.14")  # Convert string to float
print(z)  # Output: 3.14

#Type conversion is useful for manipulating data and performing operations with different types.

